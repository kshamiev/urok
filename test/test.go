package main

import (
	"fmt"
	"sync"

	"gopkg.in/sungora/app.v1/lg"
	"gopkg.in/sungora/app.v1/tool"
)

type AutoGenerated struct {
	Key      string `json:"Key"`
	Password string `json:"Password"`
}

var bbb sync.WaitGroup
var mu sync.Mutex

func main() {

	for i := 0; i < 1; i++ {
		bbb.Add(1)
		go test()
	}
	bbb.Wait()
	fmt.Println("OK")
	lg.Dumper(len(key), len(link))
	// 	lg.Dumper(response, res.StatusCode, err)
}

func test() {

	defer bbb.Done()
	var response = new(AutoGenerated)
	r := tool.NewRequest("https://password.hostkey.com/setpass")
	r.Header.SetContentType("application/json")
	r.Header.AuthorizationBasic("Inventory", "ByFIPhwipuZ7fthaAq3DnjBEJQiS6sG")
	res, err := r.GET(nil, response)
	if err != nil {
		fmt.Println(err.Error())
		return
	}
	fmt.Println(res.StatusCode, response)
	mu.Lock()
	defer mu.Unlock()
	key[response.Key] = 1
	link[response.Password] = 1
}

var key = make(map[string]int, 100000)
var link = make(map[string]int, 100000)
